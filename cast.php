<p>Jason Marsden’s career spans more than fifteen years in the entertainment industry.  His first big break was a recurring role on the daytime soap, <em>General Hospital</em> as Alan Quartermain Jr.   Shortly thereafter, he landed his first television series, <em>The Munsters Today</em>, the revamped version of the original 60s series with John Schuck and Lee Meriwether, and Jason as <em>Little Eddie Munster</em>. <em>The Munsters Today</em> ran for three seasons, then Jason was cast on such shows as, <em>Eerie Indiana</em>, <em>Almost Home: The Torkelsons</em>, <em>Tom</em>, starring Tom Arnold, and joined the cast for the last two seasons of <em>Step by Step</em>, starring Suzanne Somers and Patrick Duffy. In the interim, he held recurring roles on <em>Boy Meets World</em>, <em>Full House</em>, and <em>Blossom</em>.</p>

<p>He has worked with award-winning Director Ridley Scott as the First Mate to Jeff Bridges' Captain in <em>White Squall</em> and was cast as Billy Crystal's character, Buddy Young Jr. at age 16, in <em>Mr. Saturday Night</em>.</p>

<p>In addition, Jason has guest-starred on such must-see TV hits as <em>Will and Grace</em> playing Will's unfortunately under-tall blind-date and <em>Just Shoot Me</em> as the grown up bully from Elliot's past.  He played &ldquo;Bug&rdquo; in HBO’s and Stan Winston's late night horror film, <em>How to Make a Monster</em>, and starred in the much anticipated <em>Return to the Batcave: the Misadventures of Adam and Burt</em> for CBS, with Jason as the Young Burt Ward.</p>

<p>Jason’s also been fortunate enough to have an outstanding career in voiceover.  Lending his voice to many animated films and series, one of his favorites is <em>Max</em>, Goofy's son, in the hit feature film: <em>A Goofy Movie</em> and then reprised the role in the straight to video sequel <em>An Extremely Goofy Movie</em>.  Among Jason's many voiceover credits are, the Academy Award® winning: <em>Spirited Away</em>, <em>The Lion King 2: Simba's Pride</em>, Disney's <em>Tarzan</em>, <em>Static Shock</em>  on the WB, <em>Justice League</em> on the Cartoon Network, <em>Fairly Odd Parents</em> on Nickelodeon in addition to many more.</p>
  
<p>Jason is a proud member of the DGA, having directed the season one finale of Nickelodeon's hit series: <em>The Journey of Allen Strange</em> for Lynch Entertainment. Jason is quickly expanding his career into writing and directing.</p>
